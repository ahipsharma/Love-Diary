function createDiaryView(diary) {
    const div = document.createElement("div");
    div.className = "diary-view";

    // рЯФ• HANDLE IMAGE TYPE HERE
    if (diary.type === "image") {
        div.innerHTML = `
            <h2>${diary.title}</h2>
            <img src="${diary.image}" class="diary-full-image" />
            <button class="close-btn">вЖР Close</button>
        `;
    } else if (diary.type === "video") {
        div.innerHTML = `
            <h2>${diary.title}</h2>
            <video width="130" height="240" controls>
                <source src="${diary.video}" type="video/mp4" class="diary-full-image">
            </video>
            <button class="close-btn">вЖР Close</button>
        `;
    } else {
        div.innerHTML = `
            <h2>${diary.title}</h2>
            <div class="diary-content">
                ${diary.content.replace(/\n/g, "<br>")}
            </div>

            <button class="close-btn">вЖР Close</button>
        `;
    }

    return div;
}
let activeDiaryView = null;

function openDiary(diary, cardElement) {
    // remove existing open diary
    if (activeDiaryView) {
        activeDiaryView.remove();
        activeDiaryView = null;
    }

    const diaryView = createDiaryView(diary);

    // рЯФ• insert right AFTER clicked card
    cardElement.after(diaryView);

    activeDiaryView = diaryView;

    // close button
    diaryView.querySelector(".close-btn").onclick = () => {
        diaryView.remove();
        activeDiaryView = null;
    };

    unlockBadge("FIRST_READ");

    // smooth scroll into view
    diaryView.scrollIntoView({
        behavior: "smooth",
        block: "start"
    });
}

